export function createSeedData(): AppData {
  const subscriptions: Subscription[] = subscriptionSeeds.map((subscription) => ({
    ...subscription,
    nextDueDate: calculateNextDueDate(subscription.startDate, subscription.billingCycle),
  }));

  const paymentHistory = subscriptions.flatMap((subscription) =>
    buildPaymentTimeline(subscription).map((entry, index) => ({
      id: `${subscription.id}-${index}-${format(new Date(entry.date), "yyyyMMdd")}`,
      subscriptionId: subscription.id,
      date: entry.date,
      amountCents: entry.amountCents,
      currency: subscription.currency,
      note: "Auto-generated demo payment",
    })),
  );

  return {
    categories,
    paymentMethods,
    subscriptions,
    paymentHistory,
    settings: {
      defaultCurrency: "EUR",
      appearance: "light",
    },
  };
}
